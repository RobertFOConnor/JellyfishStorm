package com.yellowbyte.jjss.wave;

public class WavePrototype {
	public String enemyType; // TYPE OF ENEMY.
	public int amount; // NUMBER OF ENEMIES IN THE WAVE.
	public int speed; // THE SPEED THE ENEMIES IN THE WAVE WILL MOVE.
	public String pattern; // HOW THE ENEMIES WILL MOVE. E.G. DIAGONALLY / SINE WAVE
	public String entry; // WHAT SIDE OF THE SCREEN ENEMIES WQILL ENTER FROM. RIGHT | LEFT | TOP | BOTTOM.
	public int position; // WHERE THE ENEMIES WILL START.
}
